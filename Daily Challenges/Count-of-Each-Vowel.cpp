#include <bits/stdc++.h>
 
using namespace std;

int main(int argc, char** argv)
{
    string s;
    cin>>s;
    cout<<"a "<<count(s.begin(),s.end(),'a')<<endl;
    cout<<"e "<<count(s.begin(),s.end(),'e')<<endl;
    cout<<"i "<<count(s.begin(),s.end(),'i')<<endl;
    cout<<"o "<<count(s.begin(),s.end(),'o')<<endl;
    cout<<"u "<<count(s.begin(),s.end(),'u')<<endl;


}